if (!function_exists('tknIsUserPage')) {
    function tknIsUserPage(?string $scriptName = null): bool {
        if (defined('TKN_ROUTED_USER_PAGE')) {
            return TKN_ROUTED_USER_PAGE === true;
        }

        $scriptName = $scriptName ?? ($_SERVER['SCRIPT_NAME'] ?? '');
        return in_array(basename($scriptName), tknUserPageNames(), true);
    }
}

