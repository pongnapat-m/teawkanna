if (!function_exists('tknTranslateUi')) {
    function tknTranslateUi(string $output, string $lang): string {
        if ($lang !== 'en') return $output;

        $translations = tknUiTranslations();

        // These short words also occur inside activity names and descriptions
        // stored in the database. Translating them alone creates mixed-language
        // content, so only their longer UI phrases are translated.
        foreach ([
            'กิจกรรม', 'จาก', 'ถึง', 'สถานที่', 'รีวิว', 'เรียนรู้',
            'ลงมือทำ', 'ธรรมชาติ', 'เกษตร', 'วัฒนธรรม', 'ชุมชน',
            'ผจญภัย', 'เลือก', 'คน', 'เมนู',
        ] as $unsafeKey) {
            unset($translations[$unsafeKey]);
        }

        uksort($translations, static fn($a, $b) => mb_strlen($b) <=> mb_strlen($a));
        $output = str_replace(array_keys($translations), array_values($translations), $output);

        $patterns = [
            '/ผลการค้นหา:\s*Found\s*(\d+)\s*Activities/u' => 'Search results: $1 activities',
            '/Found\s*(\d+)\s*Activities/u' => 'Found $1 activities',
            '/(\d+)\s*Activities/u' => '$1 activities',
            '/(\d+)\s*คน/u' => '$1 people',
            '/(\d+)\s*ดวง/u' => '$1 stamps',
            '/อีก\s*(\d+)\s*stamps/u' => '$1 more stamps',
            '/฿([\d,.]+)\s*\/\s*person/u' => 'THB $1 / person',
            '/THB\s*([\d,.]+)\s*\/\s*person/u' => 'THB $1 / person',
        ];

        return preg_replace(array_keys($patterns), array_values($patterns), $output);
    }
}

if (!function_exists('tknLanguageSwitcherHtml')) {
    function tknLanguageSwitcherHtml(string $lang): string {
        $next = $lang === 'en' ? 'th' : 'en';
        $label = strtoupper($next);
        $href = function_exists('addLangParam')
            ? addLangParam($_SERVER['REQUEST_URI'] ?? '/tkn/home', $next)
            : '?lang=' . $next;

        return '<a class="tkn-global-lang-switch" href="'
             . htmlspecialchars($href, ENT_QUOTES, 'UTF-8')
             . '" aria-label="Switch language">' . $label . '</a>';
    }
